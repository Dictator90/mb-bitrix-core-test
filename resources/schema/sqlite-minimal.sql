    DOC_ROOT TEXT,
    DOMAIN_LIMITED TEXT NOT NULL DEFAULT 'N',
    SERVER_NAME TEXT,
    SITE_NAME TEXT,
    EMAIL TEXT,
    CULTURE_ID INTEGER
);

