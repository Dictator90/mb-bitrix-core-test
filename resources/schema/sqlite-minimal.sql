-- Minimal Bitrix tables for test prolog bootstrap (SQLite).

CREATE TABLE IF NOT EXISTS b_option (
    MODULE_ID TEXT NOT NULL,
    NAME TEXT NOT NULL,
    VALUE TEXT,
    DESCRIPTION TEXT,
    SITE_ID TEXT NOT NULL,
    PRIMARY KEY (MODULE_ID, NAME, SITE_ID)
);

CREATE TABLE IF NOT EXISTS b_module (
    ID TEXT NOT NULL PRIMARY KEY,
    DATE_ACTIVE TIMESTAMP NULL
);

INSERT OR IGNORE INTO b_module (ID) VALUES ('main');

CREATE TABLE IF NOT EXISTS b_module_to_module (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    TIMESTAMP_X TEXT,
    SORT INTEGER NOT NULL DEFAULT 100,
    FROM_MODULE_ID TEXT NOT NULL,
    MESSAGE_ID TEXT NOT NULL,
    TO_MODULE_ID TEXT NOT NULL,
    TO_PATH TEXT,
    TO_CLASS TEXT,
    TO_METHOD TEXT,
    TO_METHOD_ARG TEXT,
    VERSION INTEGER,
    UNIQUE_ID TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS b_lang (
    LID TEXT NOT NULL PRIMARY KEY,
