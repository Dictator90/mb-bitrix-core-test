CREATE TABLE IF NOT EXISTS b_culture (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    CODE TEXT,
    NAME TEXT,
    FORMAT_DATE TEXT,
    FORMAT_DATETIME TEXT,
    FORMAT_NAME TEXT,
    WEEK_START INTEGER DEFAULT 1,
    CHARSET TEXT,
    DIRECTION TEXT DEFAULT 'Y',
    SHORT_DATE_FORMAT TEXT DEFAULT 'n/j/Y',
    MEDIUM_DATE_FORMAT TEXT DEFAULT 'M j, Y',
    LONG_DATE_FORMAT TEXT DEFAULT 'F j, Y',
    FULL_DATE_FORMAT TEXT DEFAULT 'l, F j, Y',
    DAY_MONTH_FORMAT TEXT DEFAULT 'F j',
    DAY_SHORT_MONTH_FORMAT TEXT DEFAULT 'M j',
    DAY_OF_WEEK_MONTH_FORMAT TEXT DEFAULT 'l, F j',
    SHORT_DAY_OF_WEEK_MONTH_FORMAT TEXT DEFAULT 'D, F j',
    SHORT_DAY_OF_WEEK_SHORT_MONTH_FORMAT TEXT DEFAULT 'D, M j',
    SHORT_TIME_FORMAT TEXT DEFAULT 'g:i a',
    LONG_TIME_FORMAT TEXT DEFAULT 'g:i:s a',
    AM_VALUE TEXT DEFAULT 'am',
    PM_VALUE TEXT DEFAULT 'pm',
    NUMBER_THOUSANDS_SEPARATOR TEXT DEFAULT ',',
    NUMBER_DECIMAL_SEPARATOR TEXT DEFAULT '.',
    NUMBER_DECIMALS INTEGER DEFAULT 2
